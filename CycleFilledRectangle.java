public class CycleFilledRectangle {
    /*
4. 3 Заполненный прямоугольник
*/
    public static void main(String[] args) {
        int x = 0;
        while (x < 5) {
            int y = 0;
            while (y < 10) {
                System.out.print('Q');
                y++;
            }

            System.out.println();
            x++;

        }
    }
}